    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i = tablaCitas.getSelectedRow();
        String atendida = (String) tablaCitas.getValueAt(i, 4);
        if (atendida.equals("si")) {
            JOptionPane.showMessageDialog(this, "Ya se atendio este paciente", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            int identificacionPaciente = (int) tablaCitas.getValueAt(i, 0);
            int identificacionMedico = Integer.valueOf(usuarioActual.getLogin());
            java.sql.Date fecha = (java.sql.Date) tablaCitas.getValueAt(i, 2);
            int hora = (int) tablaCitas.getValueAt(i, 3);

            Cita cita = new Cita();
            cita.setIdentificacionEmpleado(identificacionMedico);
            cita.setIdentificacionPaciente(identificacionPaciente);
            cita.setFecha(fecha.toString());
            cita.setHora(hora);

            AgregarRegistro ar = new AgregarRegistro(usuarioActual, cita);
            ar.setVisible(true);
            this.dispose();

        }


    }//GEN-LAST:event_jButton1ActionPerformed

