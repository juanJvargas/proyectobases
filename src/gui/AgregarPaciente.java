    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (validar()) {
            Paciente p = new Paciente();
            p.setIdentifiacacionPaciente(Integer.valueOf(idPaciente.getText()));
            p.setNombre(nombrePaciente.getText());
            p.setDireccion(direccionPaciente.getText());
            p.setTelefono(telefonoPaciente.getText());
            p.setFechaNacimiento(fechaPaciente.getText());
            p.setActividadEconomica(actividadPaciente.getText());
            p.setSeguridadSocial(epsPaciente.getText());
            boolean resultado = controladorPaciente.agregarPaciente(p);
            if (resultado) {
                //Icon m = new ImageIcon(getClass().getResource("/gui/images/adduser.png"));
                JOptionPane.showMessageDialog(this, "Se agrego el paciente a la base de datos", "", JOptionPane.INFORMATION_MESSAGE, null);
                limpiar();

            } else {
                JOptionPane.showMessageDialog(this, "Hubo un error al agregar el paciente", "", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

