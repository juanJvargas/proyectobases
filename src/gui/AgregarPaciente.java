    private void limpiar(){
        idPaciente.setText("");
        nombrePaciente.setText("");
        direccionPaciente.setText("");
        telefonoPaciente.setText("");
        fechaPaciente.setText("");
        actividadPaciente.setText("");
        epsPaciente.setText("");
    }
    private boolean validar() {
        boolean resultado = true;
        if (idPaciente.getText().equals("") || nombrePaciente.getText().equals("") || direccionPaciente.getText().equals("") || telefonoPaciente.getText().equals("") || fechaPaciente.getText().equals("") || actividadPaciente.getText().equals("") || epsPaciente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        if (!isNumeric(idPaciente.getText())) {
            JOptionPane.showMessageDialog(this, "La identificacion del paciente debe ser un dato numerico", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        if (!isNumeric(telefonoPaciente.getText())) {
            JOptionPane.showMessageDialog(this, "El telefono del paciente debe ser un dato numerico", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        
        return resultado;
    }

    private boolean isNumeric(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

