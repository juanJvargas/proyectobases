    public boolean validar() {

        boolean resultado = true;

        if (codigo.getText().equals("") || nombre.getText().equals("") || descripcion.getText().equals("")) {
            resultado = false;
            JOptionPane.showMessageDialog(this, "Verifique que no hayan campos vacios", "", JOptionPane.INFORMATION_MESSAGE, null);
            return resultado;
        }
        if (!isNumeric(codigo.getText())) {
            resultado = false;
            JOptionPane.showMessageDialog(this, "El campo identificacion debe ser num√©rico", "", JOptionPane.INFORMATION_MESSAGE, null);
            return resultado;
        }

        return resultado;
    }

    private boolean isNumeric(String cadena) {
        try {
            Long.parseLong(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

    public void limpiar() {
        codigo.setText("");
        nombre.setText("");
        descripcion.setText("");

    }

