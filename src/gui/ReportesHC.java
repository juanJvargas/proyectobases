    private void buscarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBActionPerformed
        // TODO add your handling code here:
        if (validar()) {
            DefaultTableModel modelo = (DefaultTableModel) tablaR.getModel();
            modelo.setRowCount(0);

            String idePaciente = idPaciente.getText();
            ArrayList<Registro> re = controladorRegistro.consultarRegistroP(idePaciente);
            if (re.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hubo un error al buscar el paciente", "", JOptionPane.INFORMATION_MESSAGE, null);

            } else {
                for (int i = 0; i < re.size(); i++) {

                    Vector u = new Vector();
                    Registro aux = re.get(i);
                    u.add(aux.getNumero_registro());
                    u.add(aux.getIdentificacion_empleado());
                    u.add(aux.getFecha());
                    u.add(aux.getObservaciones());
                    Causa ca = controladorCausa.consultarCausa(String.valueOf(aux.getCodigo_causa()));
                    u.add(ca.getNombre());
                    modelo.addRow(u);
                    tablaR.setModel(modelo);
                }
            }
        }

    }//GEN-LAST:event_buscarBActionPerformed

    private boolean validar() {
        boolean resultado = true;

        if (idPaciente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Verifique la id del paciente", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        if (!isNumeric(idPaciente.getText())) {
            JOptionPane.showMessageDialog(this, "La identificacion del paciente debe ser un dato numerico", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        if (controladorPaciente.consultarPaciente(idPaciente.getText()) == null) {
            JOptionPane.showMessageDialog(this, "El paciente no se encuentra en la base de datos", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }

        return resultado;
    }

    private boolean isNumeric(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

