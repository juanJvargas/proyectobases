    private void consultarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarBActionPerformed
        // TODO add your handling code here:
        if (!idPaciente.getText().equals("")) {
            if (controladorPaciente.consultarPaciente(idPaciente.getText()) == null) {
                JOptionPane.showMessageDialog(this, "No se puede encontrar el paciente", "", JOptionPane.INFORMATION_MESSAGE, null);
            } else {
                Paciente pa = controladorPaciente.consultarPaciente(idPaciente.getText());
                codigoP = pa.getIdentifiacacionPaciente();
                
                nombrePaciente.setText(pa.getNombre());
                direccionPaciente.setText(pa.getDireccion());
                telefonoPaciente.setText(pa.getTelefono());
                fechaPaciente.setText(pa.getFechaNacimiento());
                actividadPaciente.setText(pa.getActividadEconomica());
                epsPaciente.setText(pa.getSeguridadSocial());
            }
        }
    }//GEN-LAST:event_consultarBActionPerformed

    private void limpiar() {
        idPaciente.setText("");
        nombrePaciente.setText("");
        direccionPaciente.setText("");
        telefonoPaciente.setText("");
        fechaPaciente.setText("");
        actividadPaciente.setText("");
        epsPaciente.setText("");
    }

    private boolean validar() {
        boolean resultado = true;
        if (nombrePaciente.getText().equals("") || direccionPaciente.getText().equals("") || telefonoPaciente.getText().equals("") || fechaPaciente.getText().equals("") || actividadPaciente.getText().equals("") || epsPaciente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }
        
        if (!isNumeric(telefonoPaciente.getText())) {
            JOptionPane.showMessageDialog(this, "El telefono del paciente debe ser un dato numerico", "", JOptionPane.INFORMATION_MESSAGE, null);
            resultado = false;
            return resultado;
        }

        return resultado;
    }

    private boolean isNumeric(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

